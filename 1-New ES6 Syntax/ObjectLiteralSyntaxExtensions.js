/*
The object literal is one of the most popular patterns for creating
objects in JavaScript because of its simplicity. ES6 makes the object
literal more succinct and powerful by extending the syntax in some ways.
*/

const employee={
    name:'Farhan Ahmed',
    position:'Software Engineer',
    salary:80000
}

console.log(employee.name);
console.log(employee.position);
console.log(employee.salary);